function showpics1(){
            
            button = document.getElementById('show1');
            o = document.getElementById('over1');
            s = document.getElementById('dia1');
            button.style.visibility = 'hidden';
            
            o.style.opacity = 1;
            s.style.opacity = 1;
            s.show();
            
        }

        function back1() {
            button = document.getElementById('show1');
            o = document.getElementById('over1');
            button.style.visibility = 'visible';
            
            o.style.opacity = .6;
            s.close();
        }
